package kr.ac.jejunu.kdi.model;

public class User {
	private String id;
	private String Name;
	private String Passwrod;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getPasswrod() {
		return Passwrod;
	}
	public void setPasswrod(String passwrod) {
		Passwrod = passwrod;
	}

}
